def CategoryToInteger(CategoryType, Category, Date):
    """ Assignes and integer to an OBO category|subcategory based on the date """
    CategoryLabels = ['Offence', 'Verdict', 'Punishment']
    CategoryCategories = [ OffenceCategory, VerdictCategory, PunishmentCategory]
    CategoryInteger = [OffenceInteger, VerdictInteger, PunishmentInteger]
    
    CategoryIndex = CategoryLabels.index(CategoryType)
    
    DateIndex = 0
    if 0 <= int(Date) < 1920:
            DateIndex = 0
            
    return str(CategoryInteger[CategoryIndex][DateIndex][CategoryCategories[CategoryIndex].index(Category)])

def upcaseFirstLetter(s):
    """Capitalise just the first letter of a string
    From: http://stackoverflow.com/a/13525843/398969 """
    return s[0].upper() + s[1:]

def generateCategories():
    """Fill an array with categories and subcategories concatenated """
    
    CategoryArray=['Date'] 
    
    for Category in offcat:
        CategoryArray.append(upcaseFirstLetter(Category)+'NotGuilty')
        for Punishment in puncat: 
            CategoryArray.append(upcaseFirstLetter(Category)+upcaseFirstLetter(Punishment))
                        
    for Category in offsubcat:
        CategoryArray.append(upcaseFirstLetter(Category)+'NotGuilty')
        for Punishment in punsubcat: 
            CategoryArray.append(upcaseFirstLetter(Category)+upcaseFirstLetter(Punishment))
    return CategoryArray

